array = [1, 2, 3, 4, 5]

# Accessing elements
print(array[0])  # Output: 1

# Modifying elements
array[2] = 10
print(array)  # Output: [1, 2, 10, 4, 5]

# Appending elements
array.append(6)
print(array)  # Output: [1, 2, 10, 4, 5, 6]

# Removing elements
array.remove(2)
print(array)  # Output: [1, 10, 4, 5, 6]

# Length of the array
print(len(array))  # Output: 5
